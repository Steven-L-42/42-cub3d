#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*temp;
	t_list	*new;
	t_list	*func;

	if (!lst || !f || !del)
		return (NULL);
	new = NULL;
	while (lst)
	{
		func = (f)(lst->content);
		temp = ft_lstnew(func);
		if (!temp)
		{
			free(func);
			ft_lstclear(&new, del);
			return (NULL);
		}
		ft_lstadd_back(&new, temp);
		lst = lst->next;
	}
	return (new);
}
